   <div class="headline">
    <h3 class="h4 tPadding0 bPadding0 summary"><span class="expand_section">Security Drafts Update: XML Signature Syntax and Processing 2.0; Canonical XML Version 2.0 </span></h3>
    <p class="date"><span class="published dtstart" title="2010-03-05T00:34:47-05:00">05 March 2010</span>
  | <a title="Archived: Security Drafts Update: XML Signature Syntax and Processing 2.0; Canonical XML Version 2.0 " href="/News/2010#entry-8733">Archive</a>

    </p>
</div>
    <div class="description expand_description">
      <p>The <a href="/2008/xmlsec/">XML Security Working Group</a> has published two Working Drafts: <a href="/TR/2010/WD-xmldsig-core2-20100304/">XML Signature Syntax and Processing Version 2.0</a> and <a href="/TR/2010/WD-xml-c14n2-20100304/">Canonical XML Version 2.0</a>. The first specifies XML syntax and processing rules for creating and representing digital signatures. XML Signatures can be applied to any digital content (data object), including XML. The second is a major rewrite of Canonical XML Version 1.1 to address issues around performance, streaming, hardware implementation, robustness, minimizing attack surface, determining what is signed and more. It also incorporates an update to Exclusive Canonicalization, effectively a 2.0 version, as well. Learn more about the <a href="/Security/">Security Activity</a>.</p>

    </div>
</div>

