   <div class="headline">
    <h3 class="h4 tPadding0 bPadding0 summary"><span class="expand_section">Call for Review: XML Linking Language (XLink) Version 1.1 Proposed Recommendation </span></h3>
    <p class="date"><span class="published dtstart" title="2010-02-25T18:00:07-05:00">25 February 2010</span>
  | <a title="Archived: Call for Review: XML Linking Language (XLink) Version 1.1 Proposed Recommendation " href="/News/2010#entry-8729">Archive</a>

    </p>
</div>
    <div class="description expand_description">
      <p>The <a href="/XML/Core/">XML Core Working Group</a> has published a Proposed Recommendation of <a href="/TR/2010/PR-xlink11-20100225/">XML Linking Language (XLink) Version 1.1</a>. This specification defines the XML Linking Language (XLink) Version 1.1, which allows elements to be inserted into XML documents in order to create and describe links between resources. It uses XML syntax to create structures that can describe links similar to the simple unidirectional hyperlinks of today's HTML, as well as more sophisticated links. Comments are welcome through 31 March. Learn more about the <a href="/XML/">Extensible Markup Language (XML) Activity</a>.</p>
