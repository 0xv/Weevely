    <h3 class="h4 tPadding0 bPadding0 summary"><span class="expand_section">Voice Extensible Markup Language (VoiceXML) 3.0 Draft Published</span></h3>
    <p class="date"><span class="published dtstart" title="2010-03-05T00:37:45-05:00">05 March 2010</span>
  | <a title="Archived: Voice Extensible Markup Language (VoiceXML) 3.0 Draft Published" href="/News/2010#entry-8734">Archive</a>

    </p>
</div>
    <div class="description expand_description">
      <p>The <a href="/Voice/">Voice Browser Working Group</a> has published a Working Draft of <a href="/TR/2010/WD-voicexml30-20100304/">Voice Extensible Markup Language (VoiceXML) 3.0</a>. This document specifies VoiceXML 3.0, a modular XML language for creating interactive media dialogs that feature synthesized speech, recognition of spoken and DTMF key input, telephony, mixed initiative conversations, and recording and presentation of a variety of media formats including digitized audio, and digitized video. See the <a href="/TR/2010/WD-voicexml30-20100304/diff.html">diff-marked version</a> of changes since the previous draft, and learn more about the <a href="/Voice/">Voice Browser Activity</a>.</p>

    </div>
</div>

