    <h3 class="h4 tPadding0 bPadding0 summary"><span class="expand_section">Seven Documents Related to HTML Published</span></h3>
    <p class="date"><span class="published dtstart" title="2010-03-05T00:57:23-05:00">05 March 2010</span>
  | <a title="Archived: Seven Documents Related to HTML Published" href="/News/2010#entry-8735">Archive</a>
    </p>
</div>
    <div class="description expand_description">

      <p>W3C published today seven documents related to HTML:</p> <ul class="show_items"> <li><a href="/TR/2010/WD-html5-20100304/">HTML 5</a> and <a href="/TR/2010/WD-html5-diff-20100304/">HTML5 differences from HTML4</a>. In addition, some content that was part of the HTML 5 specification has been published in two new standalone drafts: <a href="/TR/2010/WD-2dcontext-20100304/">HTML Canvas 2D Context</a> and <a href="/TR/2010/WD-microdata-20100304/">HTML Microdata</a>.</li> <li><a href="/TR/2010/WD-html-markup-20100304/">HTML: The Markup Language</a>, a first draft. This document describes the HTML markup language and provides details necessary for producers of HTML content to create documents that conform to the language. By design, it does not define related APIs, nor attempt to specify how consumers of HTML content are meant to process documents, nor attempt to be a tutorial or "how to" authoring guide.</li> <li><a href="/TR/2010/WD-rdfa-in-html-20100304/">HTML+RDFa</a>, which defines rules and guidelines for adapting the RDF in XHTML: Syntax and Processing (RDFa) specification for use in the HTML5 and XHTML5 members of the HTML family.</li> <li><a href="/TR/2010/WD-html-bidi-20100304/">Additional Requirements for Bidi in HTML</a>, a first draft. Authoring a web app that needs to support both right-to-left and left-to-right interfaces, or to take as input and display both left-to-right and right-to-left data, usually presents a number of challenges that make it an especially laborious and bug-prone task. Some of these are due to browser bugs, but some can be traced to a gap in the specification of the bidirectional aspects of a given HTML feature. And some of these challenges could be greatly simplified by adding a few strategically placed new HTML features. This document proposes fixes for some of the most repetitive pain points.</li> </ul> <p>All documents were published by the <a href="/html/wg/">HTML Working Group</a> except the last one, published by the <a href="/International/core/">Internationalization Core Working Group</a>.</p>

    </div>
</div>

